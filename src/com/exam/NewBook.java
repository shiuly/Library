    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/library", "root", "java1234");
            PreparedStatement ps = con.prepareStatement("INSERT INTO book(book_id,name,edition,publiher,price,pages)"
                    + "Values(?,?,?,?,?,?) ");
            ps.setString(1, id.getText());
            ps.setString(2, name.getText());
            ps.setString(3, (String) combo.getSelectedItem());
            ps.setString(4, pub.getText());
            ps.setInt(5, Integer.parseInt(price.getText()));
            ps.setInt(6, Integer.parseInt(pages.getText()));
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "New Book Added");

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(NewBook.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

