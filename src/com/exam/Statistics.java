    public void issueData() {
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection myconnection = DriverManager.getConnection("jdbc:mysql://localhost:3306/library", "root", "java1234");
            PreparedStatement ps = myconnection.prepareStatement("Select * from issue");

            ResultSet rs = ps.executeQuery();
            DefaultTableModel dtm = new DefaultTableModel();
            Object[] colH = new Object[]{"Book_ID", "Name","Edition", "Publisher", "Price", "Pages"};
            dtm.setColumnIdentifiers(colH);
            Object[] data = new Object[rs.getMetaData().getColumnCount()];
            while (rs.next()) {
                for (int i = 0; i < rs.getMetaData().getColumnCount(); i++) {

                    data[0] = rs.getInt(1);
                    data[1] = rs.getString(2);
                     data[2] = rs.getInt(3);
                    data[3] = rs.getString(4);
                    data[4] = rs.getInt(5);

                    data[5] = rs.getInt(6);
                }
                dtm.addRow(data);
            }

            itable.setModel(dtm);

        } catch (SQLException ex) {
            ex.printStackTrace();
        } catch (ClassNotFoundException ex) {
            ex.printStackTrace();
        }
    }
